    var num1 = 1
    var num2 = 1
    var num3 = 0
    if (n <= 2) return 1
    else while (n !== k) {
        num3 = num1 + num2
        x = revert(num3)
        while (x > 0) {
            k += 1
            if (k == n) return x % 10
            x /= 10
        }
        num1 = num2
        num2 = num3
    }
    return 0
}
